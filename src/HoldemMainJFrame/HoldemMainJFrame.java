    private void onStageChange() {
        switch (holdem.holdemState.cardStage) {
            case PRE_FLOP:
                shuffle();
                break;
            case FLOP:
                dealFlop();
                break;
            case TURN:
                dealTurn();
                break;
            case RIVER:
                dealRiver();
                break;
            case SHOW_DOWN:
                showDown();
                break;
        }
    }
    
