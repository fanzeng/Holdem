    private String toString(String[] stringArray, String delimiter) {
        String s = "";
        for (int i = 0; i < stringArray.length-1; i++) {
            s += stringArray[i] + delimiter;
        }
        s += stringArray[stringArray.length-1];
        return s;
    }

